CORS_ORIGINS=https://${domain}
PAYMENT_EXPIRY_MINUTES=30
PLAN_PRO_MONTHLY_NANOGRIN=10000000000
PLAN_PRO_YEARLY_NANOGRIN=100000000000
PLAN_LIFETIME_NANOGRIN=500000000000

# ─── Option 3: Remove / Switch Domain ─────────────────────────────────────────
opt_3_remove_switch() {
    show_banner
    section "Option 3 — Remove / Switch Domain"

    load_conf

    if [[ -z "$DOMAIN" ]]; then
        warn "No domain configured. Nothing to remove or switch."
        press_enter; return 0
    fi

    echo -e "  Configured domain: ${BOLD}${DOMAIN}${RESET}"
    echo ""
    echo -e "    ${YELLOW}a)${RESET}  Remove domain         ${DIM}nginx config + SSL cert only — files kept${RESET}"
    echo -e "    ${YELLOW}b)${RESET}  Switch to new domain  ${DIM}remove old config, set up new domain + SSL${RESET}"
    echo -e "    ${YELLOW}0)${RESET}  Back"
    echo ""
    echo -ne "  Choose [a/b/0]: "; read -r _sub

    case "${_sub,,}" in
      a)
        echo ""
        echo -ne "  ${BOLD}${YELLOW}Type 'yes' to confirm removing config for ${DOMAIN}: ${RESET}"
        read -r _c
        [[ "$_c" != "yes" ]] && info "Aborted."; [[ "$_c" != "yes" ]] && press_enter && return 0

        # Remove nginx config
        if [[ -e "$NGINX_CONF_PATH" ]]; then
            rm -f "$NGINX_CONF_PATH"
            success "Removed: $NGINX_CONF_PATH"
        fi
        if [[ -n "$NGINX_ENABLED_PATH" && -L "$NGINX_ENABLED_PATH" ]]; then
            rm -f "$NGINX_ENABLED_PATH"
            success "Removed symlink: $NGINX_ENABLED_PATH"
        fi
        if nginx -t 2>/dev/null; then
            systemctl reload nginx
            success "nginx reloaded"
        else
            warn "nginx config test failed — check /etc/nginx/ manually"
        fi

        # Remove SSL cert
        if [[ -d "/etc/letsencrypt/live/${DOMAIN}" ]]; then
            certbot delete --cert-name "$DOMAIN" --non-interactive 2>/dev/null || {
                rm -rf "/etc/letsencrypt/live/${DOMAIN}"
                rm -f  "/etc/letsencrypt/renewal/${DOMAIN}.conf"
            }
            success "SSL certificate removed for $DOMAIN"
        fi

        local _old="$DOMAIN"
        DOMAIN=""
        save_conf
        success "Domain '$_old' removed. Use Option 2 to configure a new domain."
        ;;

      b)
        echo ""
        while true; do
            echo -ne "  ${BOLD}New domain name${RESET}: "
            read -r _new
            [[ -n "$_new" ]] && break
            warn "Domain cannot be empty."
        done
        echo -ne "  ${BOLD}Email for SSL${RESET} (Enter = keep [$EMAIL]): "
        read -r _em; [[ -n "$_em" ]] && EMAIL="$_em"

        echo ""
        echo -e "  Will remove: ${BOLD}${DOMAIN}${RESET}"
        echo -e "  Will set up: ${BOLD}${_new}${RESET}"
        echo ""
        ask_proceed "Proceed" || return 0

        mkdir -p "$LOG_DIR"
        local logf="$LOG_DIR/switch_$(date +%Y%m%d_%H%M%S).log"

        (
            set -euo pipefail
            exec > >(tee "$logf") 2>&1

            section "Removing old domain: $DOMAIN"
            rm -f "$NGINX_CONF_PATH" 2>/dev/null || true
            [[ -n "$NGINX_ENABLED_PATH" ]] && rm -f "$NGINX_ENABLED_PATH" 2>/dev/null || true
            certbot delete --cert-name "$DOMAIN" --non-interactive 2>/dev/null || true
            nginx -t 2>/dev/null && systemctl reload nginx || true
            success "Old domain removed"

            DOMAIN="$_new"

            section "Setting up new domain: $DOMAIN"
            sync_frontend "$DOMAIN"
            write_nginx_http "$DOMAIN"
            get_ssl "$DOMAIN" "$EMAIL"
            write_nginx_https "$DOMAIN"
            [[ -d "$BACKEND_DIR" ]] && sync_backend || true
        ) || { warn "Errors during switch — check $logf"; press_enter; return 0; }

        DOMAIN="$_new"
        save_conf
        print_summary "$DOMAIN"
        ;;

      *) info "Back to main menu." ;;
    esac

    press_enter
}

# ─── Option 4: Set / Reset Admin Account ─────────────────────────────────────
opt_4_set_admin() {
    show_banner
    section "Option 4 — Set / Reset Admin Account"

    if [[ ! -x "$PB_DIR/pocketbase" ]]; then
        warn "PocketBase is not installed. Run Option 2 to set up the backend first."
        press_enter; return 0
    fi

    echo ""
    echo -e "  Use this option to create or reset the PocketBase admin account."
    echo -e "  The admin UI is accessible at: ${BOLD}https://${DOMAIN:-<your-domain>}/_/${RESET}"
    echo ""
    echo -ne "  Admin email (0 to cancel): "; read -r _email
    [[ "$_email" == "0" || -z "$_email" ]] && return 0

    echo -ne "  New password (min 8 chars): "; read -rs _pass; echo ""
    [[ "$_pass" == "0" || -z "$_pass" ]] && return 0
    if [[ ${#_pass} -lt 8 ]]; then
        warn "Password must be at least 8 characters."; press_enter; return 0
    fi
    echo -ne "  Confirm password:          "; read -rs _pass2; echo ""
    if [[ "$_pass" != "$_pass2" ]]; then
        warn "Passwords do not match."; press_enter; return 0
    fi

    # Ensure PocketBase is running
    if ! systemctl is-active --quiet office-tools-pb 2>/dev/null; then
        info "Starting PocketBase…"
        systemctl start office-tools-pb
        sleep 3
    fi

    "$PB_DIR/pocketbase" superuser upsert "$_email" "$_pass" \
        && success "Admin account set — you can now log in at https://${DOMAIN:-<domain>}/_/" \
        || { warn "Failed to update admin account."; press_enter; return 0; }

    # Update .env so payment server can authenticate
    if [[ -f "$BACKEND_DIR/.env" ]]; then
        sed -i "s|^PB_ADMIN_EMAIL=.*|PB_ADMIN_EMAIL=${_email}|"   "$BACKEND_DIR/.env"
        sed -i "s|^PB_ADMIN_PASSWORD=.*|PB_ADMIN_PASSWORD=${_pass}|" "$BACKEND_DIR/.env"
        systemctl restart office-tools-pay 2>/dev/null || true
        success ".env updated and payment server restarted"
    fi

    PB_ADMIN_EMAIL="$_email"
    save_conf

    # Offer to set Grin receiving address
    if [[ -f "$BACKEND_DIR/.env" ]]; then
        echo ""
        echo -ne "  Grin receiving address (Enter to skip): "; read -r _gaddr
        if [[ -n "$_gaddr" ]]; then
            if grep -q "^GRIN_RECEIVING_ADDRESS=" "$BACKEND_DIR/.env"; then
                sed -i "s|^GRIN_RECEIVING_ADDRESS=.*|GRIN_RECEIVING_ADDRESS=${_gaddr}|" "$BACKEND_DIR/.env"
            else
                echo "GRIN_RECEIVING_ADDRESS=${_gaddr}" >> "$BACKEND_DIR/.env"
            fi
            success "GRIN_RECEIVING_ADDRESS updated"
        fi
    fi

    # (Re-)create collections now that we have valid credentials
    setup_pb_collections "$_email" "$_pass"

    press_enter
}

# ─── Option 5: Update from Repo ───────────────────────────────────────────────
opt_5_update_repo() {
    show_banner
    section "Option 5 — Update from Repository"

    if [[ ! -d "$REPO_DIR/.git" ]]; then
        warn "Repository not cloned yet. Run Option 1 to install first."
        press_enter; return 0
    fi

    local current_branch
    current_branch=$(git -C "$REPO_DIR" branch --show-current 2>/dev/null || echo "unknown")

    echo -e "  Remote:         ${DIM}${REPO_URL}${RESET}"
    echo -e "  Current branch: ${BOLD}${current_branch}${RESET}"
    echo -e "  Last commit:    ${DIM}$(git -C "$REPO_DIR" log -1 --format='%h %s' 2>/dev/null)${RESET}"
    echo ""

    info "Fetching remote branch list…"
    git -C "$REPO_DIR" fetch --quiet origin 2>/dev/null \
        || warn "Could not reach remote — using cached branch list"

    local branches=()
    while IFS= read -r b; do
        [[ -z "$b" ]] && continue
        branches+=("$b")
    done < <(git -C "$REPO_DIR" branch -r 2>/dev/null \
        | grep -v '\->' | sed 's|[[:space:]]*origin/||')

    if [[ "${#branches[@]}" -gt 0 ]]; then
        echo -e "  ${BOLD}Available branches:${RESET}"
        local i=1
        for b in "${branches[@]}"; do
            local marker=""
            [[ "$b" == "$current_branch" ]] && marker=" ${DIM}← current${RESET}"
            printf "    %s%d)%s  %s%s\n" "${CYAN}" "$i" "${RESET}" "$b" "$marker"
            ((i++))
        done
        echo ""
    fi

    echo -ne "  Branch number or name (Enter = keep ${DIM}[${current_branch}]${RESET}, 0 to cancel): "
    read -r _input
    [[ "$_input" == "0" ]] && return 0

    local target_branch="$current_branch"
    if [[ -n "$_input" ]]; then
        if [[ "$_input" =~ ^[0-9]+$ ]] && (( _input >= 1 && _input <= ${#branches[@]} )); then
            target_branch="${branches[$(( _input - 1 ))]}"
        else
            target_branch="$_input"
        fi
    fi

    echo ""
    echo -e "  Target:  ${BOLD}${target_branch}${RESET}"
    ask_proceed "Proceed" || return 0

    mkdir -p "$LOG_DIR"
    local logf="$LOG_DIR/update_$(date +%Y%m%d_%H%M%S).log"
    info "Logging to: $logf"

    (
        set -euo pipefail
        exec > >(tee "$logf") 2>&1

        git -C "$REPO_DIR" fetch origin
        git -C "$REPO_DIR" checkout "$target_branch"
        git -C "$REPO_DIR" reset --hard "origin/$target_branch"
        success "Repo updated: $(git -C "$REPO_DIR" log -1 --format='%h %s')"

        load_conf
        sync_frontend "${DOMAIN:-}"

        # Sync backend if installed
        [[ -d "$BACKEND_DIR" ]] && sync_backend || true

        # Always reload nginx if it's running
        if systemctl is-active --quiet nginx 2>/dev/null; then
            systemctl reload nginx
            success "nginx reloaded"
        fi
    ) || { warn "Update failed — check $logf"; press_enter; return 0; }

    load_conf
    if [[ -n "$DOMAIN" ]]; then
        success "Updated to branch '${target_branch}' — https://${DOMAIN}"
    else
        success "Updated to branch '${target_branch}'"
    fi
    press_enter
}


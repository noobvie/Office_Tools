    echo ""
}

press_enter() {
    echo ""
    echo -ne "${DIM}  Press Enter to return to menu…${RESET}"
    read -r
}

# Returns 0 (true=proceed) or 1 (abort — n or 0 pressed)
ask_proceed() {
    local prompt="${1:-Proceed}"
    echo -ne "  ${BOLD}${prompt}? [Y/n/0]: ${RESET}"; read -r _ap
    [[ "${_ap,,}" == "n" || "$_ap" == "0" ]] && return 1
    return 0
}

